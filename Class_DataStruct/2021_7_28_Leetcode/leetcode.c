int removeDuplicates(int* nums, int numsSize)
{
    if(!numsSize)
    {
        return 0;
    }
    int src=0;
    int dest=0;
    while(src<numsSize)
    {
        if(nums[dest]==nums[src])
        {
            src++;
        }
        else
        {
            nums[dest+1]=nums[src];
            dest++;
            src++;
        }
    }
    return dest+1;

}


